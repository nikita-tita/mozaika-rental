// Функция для поиска городов
export const searchCities = (query: string): City[] => {
  const searchTerm = query.toLowerCase()
  return russianCities.filter(city => 
    city.label.toLowerCase().includes(searchTerm) ||
    city.region.toLowerCase().includes(searchTerm)
  ).slice(0, 20) // Ограничиваем результаты
}

// Функция для получения городов по региону
export const getCitiesByRegion = (region: string): City[] => {
  return russianCities.filter(city => city.region === region)
}

// Функция для получения всех регионов
export const getRegions = (): string[] => {
  return [...new Set(russianCities.map(city => city.region))].sort()
} 
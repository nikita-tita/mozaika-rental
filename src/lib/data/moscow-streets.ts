// Функция для поиска улиц
export const searchStreets = (query: string): Street[] => {
  const searchTerm = query.toLowerCase()
  return moscowStreets.filter(street => 
    street.label.toLowerCase().includes(searchTerm) ||
    street.district.toLowerCase().includes(searchTerm)
  ).slice(0, 20) // Ограничиваем результаты
}

// Функция для получения улиц по округу
export const getStreetsByDistrict = (district: string): Street[] => {
  return moscowStreets.filter(street => street.district === district)
}

// Функция для получения всех округов
export const getDistricts = (): string[] => {
  return [...new Set(moscowStreets.map(street => street.district))].sort()
}

// Функция для получения улиц по типу
export const getStreetsByType = (type: Street['type']): Street[] => {
  return moscowStreets.filter(street => street.type === type)
} 